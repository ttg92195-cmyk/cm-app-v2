// Helper functions
export const getMoviesByCategory = (categoryKey) => {
  return MOVIES.filter(movie => movie.categories.includes(categoryKey));
};

export const getMovieById = (id) => {
  return MOVIES.find(movie => movie.id === id);
};

export const searchMovies = (query) => {
  const lowerQuery = query.toLowerCase();
  return MOVIES.filter(
    movie =>
      movie.title.toLowerCase().includes(lowerQuery) ||
      movie.subtitle.toLowerCase().includes(lowerQuery) ||
      movie.director.toLowerCase().includes(lowerQuery) ||
      movie.cast.some(actor => actor.toLowerCase().includes(lowerQuery))
  );
};

export const getFeaturedMovie = () => {
  return MOVIES.find(movie => movie.id === '1');
};
